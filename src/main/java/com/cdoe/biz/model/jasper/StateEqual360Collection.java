package com.cdoe.biz.model.jasper;

import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author Tony Ford
 */
public class StateEqual360Collection {

    private List<StateEqual360Calculation> calculations = new ArrayList<StateEqual360Calculation>();
    private String fiscalYear;
    private String districtName;
    private String districtNumber;
    private String county;

    public StateEqual360Collection() {
    }

    public List<StateEqual360Calculation> getCalculations() {
        return calculations;
    }

    public void setCalculations(List<StateEqual360Calculation> calculations) {
        this.calculations = calculations;
    }

    public String getFiscalYear() {
        return fiscalYear;
    }

    public void setFiscalYear(String fiscalYear) {
        this.fiscalYear = fiscalYear;
    }

    public String getDistrictName() {
        return districtName;
    }

    public void setDistrictName(String districtName) {
        this.districtName = districtName;
    }

    public String getDistrictNumber() {
        return districtNumber;
    }

    public void setDistrictNumber(String districtNumber) {
        this.districtNumber = districtNumber;
    }

    public String getCounty() {
        return county;
    }

    public void setCounty(String county) {
        this.county = county;
    }
}
