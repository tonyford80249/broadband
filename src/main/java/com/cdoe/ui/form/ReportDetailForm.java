package com.cdoe.ui.form;

import java.util.List;

/**
 *
 * @author TonyFord
 */
public class ReportDetailForm {
 private String reportId;
 private String reportName;
 private String reportDescription;
 private Boolean districtSelectionEnabled; 
 private Boolean extraMonthSelectionEnabled;

    public ReportDetailForm() {
    }

    public ReportDetailForm(String reportName, String reportDescription, Boolean districtSelectionEnabled, String reportId) {
        this.reportId = reportId;
        this.reportName = reportName;
        this.reportDescription = reportDescription;
        this.districtSelectionEnabled = districtSelectionEnabled;
    }
    
    public ReportDetailForm(String reportName, String reportDescription, Boolean districtSelectionEnabled, Boolean extraMonthSelectionEnabled, String reportId) {
        this.reportId = reportId;
        this.reportName = reportName;
        this.reportDescription = reportDescription;
        this.districtSelectionEnabled = districtSelectionEnabled;
        this.extraMonthSelectionEnabled = extraMonthSelectionEnabled;
    }

    public String getReportName() {
        return reportName;
    }

    public void setReportName(String reportName) {
        this.reportName = reportName;
    }

    public String getReportDescription() {
        return reportDescription;
    }

    public void setReportDescription(String reportDescription) {
        this.reportDescription = reportDescription;
    }

    public Boolean getDistrictSelectionEnabled() {
        return districtSelectionEnabled;
    }

    public void setDistrictSelectionEnabled(Boolean districtSelectionEnabled) {
        this.districtSelectionEnabled = districtSelectionEnabled;
    }   

    public String getReportId() {
        return reportId;
    }

    public void setReportId(String reportId) {
        this.reportId = reportId;
    }

    public Boolean getExtraMonthSelectionEnabled() {
        return extraMonthSelectionEnabled;
    }

    public void setExtraMonthSelectionEnabled(Boolean extraMonthSelectionEnabled) {
        this.extraMonthSelectionEnabled = extraMonthSelectionEnabled;
    }
 
}
